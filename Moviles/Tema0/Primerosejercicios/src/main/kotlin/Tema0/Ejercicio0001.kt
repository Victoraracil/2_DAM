package com.dam.Tema0

fun main() {
    val num1 = 5.00f
    val num2 = 8.00f
    println("Numero 1: $num1")
    println("Numero 2: $num2")
    println("Suma: " + (num1 + num2))
    println("Resta: " + (num1 - num2))
    println("Multiplicacion: " + (num1 * num2))
    println("Division: " + (num1 / num2))
    println("Resto Division: " + (num1 % num2))
}